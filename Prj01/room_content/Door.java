package room_content;

public class Door {
	private int height;
	private int weight;
	
	
	public Door(int height, int weight) {
		super();
		this.height = height;
		this.weight = weight;
	}
	
		
//  SETTERS------------------
	public int getHeight() {
		return height;
	}
	public int getWeight() {
		return weight;
	}
	
	

}
